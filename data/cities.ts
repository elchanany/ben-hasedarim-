import { City } from '../types';

const generateCityId = (name: string): string => {
  let id = name;
  id = id.replace(/\(|\)/g, '_'); // Replace ( and ) with _
  id = id.replace(/'|"/g, '');   // Remove ' and "
  // Replace any sequence of non-alphanumeric (allowing Hebrew, English, numbers, underscore) with a single underscore
  // Also handle hyphens explicitly as they are common in city names like תל אביב-יפו
  id = id.replace(/[\s-]+/g, '_'); 
  id = id.replace(/[^א-ת0-9a-zA-Z_]/g, '_'); // General cleanup for any other special chars
  id = id.replace(/__+/g, '_');    // Normalize multiple underscores to one
  id = id.replace(/^_+|_+$/g, ''); // Trim leading/trailing underscores
  return id;
};

